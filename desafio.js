let modoOscuro = true;

const buttonModoOscuro = addEventListener("click", toggleModoOscuro);

function toggleModoOscuro(){
    if(modoOscuro == true)
    {modoOscuro = false;
        alert("Modo oscuro Activado");
    }else{modoOscuro = true;
        alert("Modo oscuro Desactivado");
    }
} 
           





   



   


